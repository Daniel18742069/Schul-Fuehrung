        <h1>Cookies</h1>


        <h3>Was sind Cookies?</h3>
        <p>
            Cookies sind kleine Textdateien, die kleine Mengen von Informationen enthalten und auf Ihrem Computer oder mobilen Gerät heruntergeladen werden,
            wenn Sie eine Website besuchen. Cookies werden dann bei jedem künftigen Besuch zurück an die Website gesendet, von der sie stammen, oder auch an
            eine andere Website, die das Cookie erkennt. Cookies sind nützlich, weil Webseiten damit die Geräte einzelner Benutzer erkennen können
            (wie beispielsweise Kennwörter, die gewählte Sprache, die Schriftgröße, andere Display-Einstellungen). Cookies erlauben es so, eine Webseite an
            Ihre Interessen anzupassen, damit diese nicht jedes Mal neu eingeben werden müssen. Cookies werden daher verwendet, um Computer-Authentifizierungen
            durchführen, Besuche zu überwachen und das Verhalten der Benutzer auf einer Website zu beobachten. Sie können auch einen eindeutigen
            Identifizierungskodex beinhalten, der für statistische Zwecke oder für Werbung die Navigation der Benutzer innerhalb der Webseite nachverfolgt.
            Während der Navigation auf der Webseite können Sie auf Ihrem Computer oder mobilen Gerät auch Cookies anderer Webseiten oder Web-Server erhalten
            (siehe „Drittanbieter-Cookies“). Einige Funktionalitäten könnten ohne Cookies nicht oder nicht richtig funktionieren, daher sind sie in bestimmten
            Fällen für das einwandfreie Funktionieren der Webseite technisch notwendig.
        </p>
        <p>
            Es gibt verschiedene Arten von Cookies. Entsprechend ihren Eigenschaften und Funktionen können diese auf Ihrem Computer oder auf Ihrem mobilen
            Gerät unterschiedlich lang gespeichert bleiben: die Sessions-Cookies, die automatisch gelöscht werden, sobald der Browser geschlossen wird;
            die so genannten dauerhaften Cookies, die auf dem Gerät des Benutzers bis zu einem vorher festgelegten Zeitpunkt bleiben.
        </p>
        <p>
            Unter den geltenden Rechtsvorschriften in Italien ist für die Verwendung von Cookies nicht immer die ausdrückliche Zustimmung erforderlich.
            Insbesondere die sog. „<strong>technischen</strong>“ Cookies erfordern diese nicht, etwa zur Übertragung einer Nachricht über ein elektronisches
            Kommunikationsnetzwerk, oder um einen vom Benutzer explizit angeforderten Dienst bereitzustellen. Es handelt sich, mit anderen Worten, um
            unverzichtbare Cookies für das einwandfreie Funktionieren der Seite oder für Aktivitäten, die der Benutzer anfordert.
        </p>
        <p>
            Zu den technischen Cookies, die nicht die ausdrückliche Zustimmung zu deren Verwendung benötigen, zählt die Behörde für den Schutz
            personenbezogener Daten folgende Cookies (siehe. Prov.Gen. „<a href="http://www.garanteprivacy.it/web/guest/home/docweb/-/docweb-display/docweb/3118884">
                Erkennung vereinfachter Verfahren für die Information und den Erhalt der Zustimmung für die Verwendung der Cookies - 8. Mai 2014</a>“):
        </p>
        <ul>
            <li>„Analyse-Cookies“, direkt eingesetzt durch den Seitenbetreiber, um Informationen bezüglich Anzahl und Verhalten der Benutzer ebendieser Webseite zu sammeln,</li>
            <li>Cookies für die Navigation oder&nbsp;Sitzungs-Cookies (für die Authentifizierung, einen Kauf zu tätigen, usw.),</li>
            <li>Funktions-Cookies: Diese Cookies ermöglichen den Benutzern die Navigation aufgrund von einer Reihe von gewählter Funktionen (wie Spracheinstellungen, die zum Kauf gewählten Waren) mit dem Zweck, Komfort und Leistung der Webseite zu verbessern.</li>
        </ul>
        <p>Für Cookies, die zur Erstellung von Nutzer-Profilen zur Erstellung von Werbebotschaften ausgerichtet sind, ist eine vorherige Zustimmung des
            Benutzers notwendig und erfolgt in Einklang mit seinen Vorlieben, die aus der Navigation zu erkennen sind.
        </p>



